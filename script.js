// Language Switcher Logic
const langSelector = document.getElementById('language-switcher');
const htmlElement = document.documentElement;

langSelector.addEventListener('change', (event) => {
    const selectedLang = event.target.value;
    setLanguage(selectedLang);
});

function setLanguage(lang) {
    const texts = document.querySelectorAll("[data-i18n]");
    texts.forEach(text => {
        const key = text.getAttribute("data-i18n");
        if (translations[lang] && translations[lang][key]) {
            text.innerHTML = translations[lang][key]; // Using innerHTML to preserve spans or icons inside if any
        }
    });
    
    // Switch Directionality and Fonts based on language
    if(lang === 'ar') {
        htmlElement.setAttribute('dir', 'rtl');
        htmlElement.setAttribute('lang', 'ar');
        document.body.classList.add('rtl-layout');
    } else {
        htmlElement.setAttribute('dir', 'ltr');
        htmlElement.setAttribute('lang', lang);
        document.body.classList.remove('rtl-layout');
    }
}

